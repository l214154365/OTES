package otes.entity;


public class clas {
		public int clas_ID;
		public String clas_Name;
		public int clas_Type;
		
		public int getClas_ID() {
			return clas_ID;
		}
		public void setClas_ID(int clas_ID) {
			this.clas_ID = clas_ID;
		}
		public String getClas_Name() {
			return clas_Name;
		}
		public void setClas_Name(String clas_Name) {
			this.clas_Name = clas_Name;
		}
		public int getClas_Type() {
			return clas_Type;
		}
		public void setClas_Type(int clas_Type) {
			this.clas_Type = clas_Type;
		}
		
}
